package com.bean;

public class Like {
	private int likeID;
	private int dynamicID;
	private int userID;
	
	public int getId() {
		return likeID;
	}
	public void setId(int id) {
		this.likeID = id;
	}
	public int getDynamicID() {
		return dynamicID;
	}
	public void setDynamicID(int dynamicID) {
		this.dynamicID = dynamicID;
	}
	public int getUserID() {
		return userID;
	}
	public void setUserID(int userID) {
		this.userID = userID;
	}
	
}
